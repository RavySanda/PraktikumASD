package jobsheet10;

public class Mahasiswa {
    public String nim;
    public String nama;
    public String kelas;
    public int absen;

    public Mahasiswa(String nim, String nama, String kelas, int absen) {
        this.nim = nim;
        this.nama = nama;
        this.kelas = kelas;
        this.absen = absen;
    }
}